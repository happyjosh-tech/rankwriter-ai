	public static function platform( $key ) {
		$all = self::platforms();
		return $all[ $key ] ?? null;
	}

	/* ============================ Generation ============================ */

	/**
	 * Generate a repurposed payload for a single post + platform via Claude.
	 *
	 * @return array|WP_Error
	 */
	public function generate( $post_id, $platform ) {
		$post = get_post( $post_id );
		if ( ! $post ) {
			return new WP_Error( 'rwai_no_post', __( 'Post not found.', 'rankwriter-ai' ) );
		}
		$cfg = self::platform( $platform );
		if ( ! $cfg ) {
			return new WP_Error( 'rwai_bad_platform', __( 'Unknown platform.', 'rankwriter-ai' ) );
		}
		if ( ! class_exists( 'RankWriter_AI_Claude_Client' ) ) {
			return new WP_Error( 'rwai_no_client', __( 'Claude client unavailable.', 'rankwriter-ai' ) );
		}
		$client = new RankWriter_AI_Claude_Client();
		if ( ! $client->is_configured() ) {
			return new WP_Error( 'rwai_no_key', __( 'Claude API key not configured.', 'rankwriter-ai' ) );
		}

		$permalink   = get_permalink( $post_id );
		$canonical   = $cfg['canonical_safe'] ? $permalink : '';
		$brand_voice = '';
		if ( class_exists( 'RankWriter_AI_Voice_Memory' ) ) {
			$cats        = wp_get_post_categories( $post_id );
			$brand_voice = ( new RankWriter_AI_Voice_Memory() )->to_prompt_context( ! empty( $cats ) ? (int) $cats[0] : 0 );
		}

		$system = $cfg['system_prompt'] . "\n\n"
			. "Audience: external readers who have never read your blog.\n"
			. "Original source URL (for the link/CTA): " . $permalink . "\n"
			. ( $canonical ? "Canonical URL is supported on this platform. The platform will be told to canonical-attribute to the source.\n" : "Canonical is NOT supported here — rewrite must differ substantively from the original.\n" )
			. ( $brand_voice ? "\n" . $brand_voice . "\n\n" : "\n" )
			. "Return ONLY a single valid JSON object with this exact shape (no markdown fences, no commentary):\n"
			. "{\n"
			. "  \"title\": \"<external-platform headline>\",\n"
			. "  \"body\": \"<full body, HTML or plain text appropriate to the platform>\",\n"
			. "  \"cta\": \"<single-sentence CTA pointing back to the source, with the URL inline>\",\n"
			. "  \"hashtags\": [\"<tag1>\", \"<tag2>\", \"<tag3>\"],\n"
			. "  \"compliance_notes\": \"<one-line callout of any platform rule the user should double-check before posting>\"\n"
			. "}";

		// Build a tight user prompt — title + clean body + target words.
		$body_text = wp_strip_all_tags( $post->post_content );
		// Cap input length to keep context window healthy.
		if ( str_word_count( $body_text ) > 3500 ) {
			$body_text = implode( ' ', array_slice( preg_split( '/\s+/', $body_text ), 0, 3500 ) ) . ' …';
		}

		$user = "Original title: " . $post->post_title . "\n\n"
			. "Original body:\n" . $body_text . "\n\n"
			. "Target word count for the repurposed version: ~" . (int) $cfg['target_words'] . " words (hard min " . (int) $cfg['min_words'] . ", hard max " . (int) $cfg['max_words'] . ").";

		$raw = $client->send( $system, array(
			array( 'role' => 'user', 'content' => $user ),
		) );
		if ( is_wp_error( $raw ) ) {
			return $raw;
		}
		$parsed = $this->parse_json( $raw );
		if ( ! is_array( $parsed ) || empty( $parsed['body'] ) ) {
			return new WP_Error( 'rwai_bad_response', __( 'Claude response could not be parsed.', 'rankwriter-ai' ) );
		}

		$hashtags = isset( $parsed['hashtags'] ) && is_array( $parsed['hashtags'] ) ? array_slice( array_map( 'sanitize_text_field', $parsed['hashtags'] ), 0, 8 ) : array();

		$payload = array(
			'title'             => sanitize_text_field( (string) ( $parsed['title'] ?? '' ) ),
			'body'              => wp_kses_post( (string) $parsed['body'] ),
			'cta'               => sanitize_text_field( (string) ( $parsed['cta'] ?? '' ) ),
			'hashtags'          => $hashtags,
			'compliance_notes'  => sanitize_text_field( (string) ( $parsed['compliance_notes'] ?? '' ) ),
			'canonical_url'     => $canonical,
			'platform'          => $platform,
		);

		// Save as a draft row in the syndication log.
		if ( class_exists( 'RankWriter_AI_Syndication_DB' ) ) {
			$payload['log_id'] = RankWriter_AI_Syndication_DB::insert( array(
				'post_id'            => (int) $post_id,
				'platform'           => $platform,
				'status'             => RankWriter_AI_Syndication_DB::STATUS_DRAFT,
				'canonical_used'     => $canonical ? 1 : 0,
				'generated_title'    => $payload['title'],
				'generated_body'     => $payload['body'],
				'generated_cta'      => $payload['cta'],
				'generated_hashtags' => wp_json_encode( $hashtags ),
				'compliance_notes'   => $payload['compliance_notes'],
			) );
		}

		return $payload;
	}

	/**
	 * Mark a previously-generated syndication as live, recording the
	 * external URL the user pasted in.
	 */
	public function mark_published( $log_id, $external_url ) {
		if ( ! class_exists( 'RankWriter_AI_Syndication_DB' ) ) {
			return false;
		}
		return RankWriter_AI_Syndication_DB::update( $log_id, array(
			'status'       => RankWriter_AI_Syndication_DB::STATUS_PUBLISHED,
			'external_url' => esc_url_raw( $external_url ),
			'published_at' => current_time( 'mysql' ),
		) );
	}

	public function delete_syndication( $log_id ) {
		if ( ! class_exists( 'RankWriter_AI_Syndication_DB' ) ) {
			return false;
		}
		return RankWriter_AI_Syndication_DB::delete( $log_id );
	}

	protected function parse_json( $raw ) {
		$raw = trim( (string) $raw );
		if ( '' === $raw ) { return null; }
		$decoded = json_decode( $raw, true );
		if ( is_array( $decoded ) ) { return $decoded; }
		if ( preg_match( '/\{.*\}/s', $raw, $m ) ) {
			$decoded = json_decode( $m[0], true );
			if ( is_array( $decoded ) ) { return $decoded; }
			$cleaned = stripslashes( $m[0] );
			$decoded = json_decode( $cleaned, true );
			if ( is_array( $decoded ) ) { return $decoded; }
		}
		return null;
	}
}
