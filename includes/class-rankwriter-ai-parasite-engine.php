/**
 * Parasite SEO Engine.
 *
 * Repurposes published WP posts for external platforms (Medium, LinkedIn,
 * Quora, Reddit, generic). Each platform has a distinct rewrite strategy
 * because the SEO and compliance rules differ sharply:
 *
 *   - Medium  → supports rel=canonical. Can be full-length faithful rewrite.
 *   - LinkedIn → NO canonical. Must be substantially rewritten so it
 *                doesn't compete with the original for the same query.
 *   - Quora    → Answers a SPECIFIC question, first-person, link in source.
 *   - Reddit   → Native summary only, never the full article. 9:1 rule
 *                compliance warning surfaced to the user.
 *   - Generic  → Customizable wrapper for any other platform.
 *
 * Output is generated by Claude given a tightly-scoped per-platform system
 * prompt. Each generation is stored in the syndications log so the admin
 * can copy → paste into the platform UI, then mark "published" with the
 * external URL.
 */
